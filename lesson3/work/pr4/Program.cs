using func;

// Задача 22: Напишите программу, которая принимает на вход число (N) и выдаёт таблицу квадратов чисел от 1 до N.
// 5 -> 1, 4, 9, 16, 25.
// 2 -> 1,4

// -------------------------------------------------------------------

int num = rw.getInt("Введите число", true);
for (int i = 1; i <= num; i++) {
	if (i != 1) rw.echo(", ", false);
	rw.echo($"{Math.Pow(i, 2)}", false);
}

