// Задача 24: Напишите программу, которая принимает на вход число (А) и выдаёт сумму чисел от 1 до А.
// 7 -> 28
// 4 -> 10
// 8 -> 36

// -------------------------------------------------------------------

int num = rw.getInt();
int sum = 0;
for (int i = 1; i <= num; i++) sum += i;
rw.echo($"Сумма чисел от 1 до {num} = {sum}");

