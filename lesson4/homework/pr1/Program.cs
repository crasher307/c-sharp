using func;

// Задача 25: Напишите цикл, который принимает на вход два числа (A и B) и возводит число A в натуральную степень B.
// 3, 5 -> 243 (3⁵)
// 2, 4 -> 16

// -------------------------------------------------------------------

int num1 = rw.getInt("Введите число 1");
int num2 = rw.getInt("Введите число 2");
double res = Math.Round(Math.Pow(num1, num2), 2);
rw.echo($"{num1}^{num2} = {res}");

